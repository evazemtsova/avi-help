def format_chunk(idx: int, chunk_id: str, category: str, title: str,
                 section: str | None, text: str) -> str:
    section_part = f" / раздел: {section}" if section else ""
    header = (
        f"[{idx}] chunk_id={chunk_id} | категория: {category}"
        f"{section_part} | статья: {title}"
    )
    return f"{header}\n{text}"
